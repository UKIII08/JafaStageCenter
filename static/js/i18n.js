var currentLang = 'pl';

function initI18n(lang) {
    if (!lang) lang = localStorage.getItem('appLang') || 'pl';
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    document.documentElement.setAttribute('data-lang', lang);
    applyTranslations(lang);
}

function applyTranslations(lang) {
    var tr = translations[lang] || translations['pl'];
    document.querySelectorAll('[data-t]').forEach(function(el) {
        var key = el.getAttribute('data-t');
        if (tr[key]) el.innerHTML = tr[key];
    });
    document.querySelectorAll('[data-t-ph]').forEach(function(el) {
        var key = el.getAttribute('data-t-ph');
        if (tr[key]) el.placeholder = tr[key];
    });
    document.querySelectorAll('[data-t-title]').forEach(function(el) {
        var key = el.getAttribute('data-t-title');
        if (tr[key]) el.title = tr[key];
    });
    var langSelect = document.getElementById('lang-select');
    if (langSelect) langSelect.value = lang;
}

function t(key) {
    return (translations[currentLang] && translations[currentLang][key]) || (translations['pl'] && translations['pl'][key]) || key;
}
